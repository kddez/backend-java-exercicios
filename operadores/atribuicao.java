public class atribuicao {
    

    public static void main(String[] args){

        int a = 10;
        int b = 5;
        

         b += a; // b = b+a;
         b -= a; // b = b-a;
         b *= a; // b = b*a;
         b /= a; // b = b/a;
         b %= a; // b é igual ao resto da divisão entre b e a;
         
    }
}
