public class relacionais {
    

    public static void main(String[] args){


        int a =20;
        int b=30;

        // == (igual)
        // != (Diferente)
        // <  (Menor que)
        // >  (Maior que)
        // <= (Menor ou igual)
        // >= (Maior ou igual)
        

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
}
